export const TAXONOMY_CATEGORY_NAMES = Object.keys(CATEGORY_TAXONOMY) as Array<
  keyof typeof CATEGORY_TAXONOMY
>;

export function listTaxonomyCategorySlugs(): readonly string[] {
  return TAXONOMY_CATEGORY_NAMES.map((name) => CATEGORY_TAXONOMY[name].slug);
}

export function getTaxonomyEntryBySlug(slug: string): CategoryTaxonomyEntry | undefined {
  return TAXONOMY_CATEGORY_NAMES.map((name) => CATEGORY_TAXONOMY[name]).find(
    (entry) => entry.slug === slug,
  );
}

export function getTaxonomyEntryByTitle(title: string): CategoryTaxonomyEntry | undefined {
  return CATEGORY_TAXONOMY[title];
}
