/**
 * Pure PDF line builder for SC-001. No DOM, no jsPDF — tested in node.
 * SC-001 has no cost breakdown, so the report lists governing metrics + steps.
 */
export interface WeldPdfLine { text: string; style: 'title' | 'hero' | 'head' | 'row' | 'warn' | 'note'; }

export interface WeldPdfInput {
  toolCode: string;
  finalLegMm: string;
  finalLegIn: string;
  utilization: string;
  jointType: string;
  requiredThroatMm: string;
  minLegMm: string;
  warnings: Array<{ severity: string; message: string }>;
  steps: Array<{ step: number; description: string; result: string }>;
}

export function buildWeldReportLines(input: WeldPdfInput): WeldPdfLine[] {
  const lines: WeldPdfLine[] = [];
  lines.push({ text: `SectorCalc — ${input.toolCode} Weld Thickness`, style: 'title' });
  lines.push({ text: `Required leg: ${input.finalLegMm} mm (${input.finalLegIn} in)`, style: 'hero' });
  lines.push({ text: `${input.jointType} · utilization ${input.utilization} · min leg ${input.minLegMm} mm · throat ${input.requiredThroatMm} mm`, style: 'note' });
  if (input.warnings.length > 0) {
    lines.push({ text: 'Heads up', style: 'head' });
    for (const w of input.warnings) lines.push({ text: `[${w.severity}] ${w.message}`, style: 'warn' });
  }
  lines.push({ text: 'Show me the math', style: 'head' });
  for (const s of input.steps) lines.push({ text: `${s.step}. ${s.description} = ${s.result}`, style: 'row' });
  lines.push({ text: 'Generated by SectorCalc — deterministic engine. Verify with the code edition in force.', style: 'note' });
  return lines;
}
