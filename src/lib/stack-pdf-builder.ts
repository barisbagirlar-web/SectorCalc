export interface StackPdfLine { text: string; style: 'title' | 'head' | 'row' | 'kv' | 'warn' | 'note'; }
export interface StackPdfComponent { name: string; nominal: string; tol: string; distribution: string; pct: string; }
export interface StackPdfInput {
  toolCode: string;
  nominalSum: string; worstPlus: string; rssPlus: string;
  mcMean: string; mcStd: string; mcP0013: string; mcP9987: string;
  cp: string; cpk: string; ppm: string;
  seed: number; iterations: number;
  components: StackPdfComponent[];
  warnings: Array<{ severity: string; message: string }>;
  steps: Array<{ step: number; description: string; result: string }>;
  checksum: string;
}

export function buildStackReportLines(input: StackPdfInput): StackPdfLine[] {
  const lines: StackPdfLine[] = [];
  lines.push({ text: `SectorCalc — ${input.toolCode} Statistical Tolerance Stack-Up`, style: 'title' });
  lines.push({ text: `Verdict: ${Number(input.cpk) >= 1 ? 'CAPABLE (Cpk >= 1.0)' : 'NOT CAPABLE (Cpk < 1.0)'}`, style: 'head' });
  lines.push({ text: `Nominal sum: ${input.nominalSum}`, style: 'kv' });
  lines.push({ text: `Worst-case +/- ${input.worstPlus}    RSS +/- ${input.rssPlus}`, style: 'kv' });
  lines.push({ text: `Monte Carlo: mean ${input.mcMean}, std ${input.mcStd}, P0.13 ${input.mcP0013}, P99.87 ${input.mcP9987}`, style: 'kv' });
  lines.push({ text: `Cp ${input.cp}    Cpk ${input.cpk}    Defect ${input.ppm} ppm`, style: 'kv' });
  lines.push({ text: `Deterministic stamp: seed ${input.seed}, ${input.iterations} runs, SHA-256 ${input.checksum}`, style: 'note' });
  lines.push({ text: 'Components', style: 'head' });
  for (const c of input.components) lines.push({ text: `${c.name}  ${c.nominal} +/- ${c.tol}  (${c.distribution}, ${c.pct}%)`, style: 'row' });
  if (input.warnings.length > 0) {
    lines.push({ text: 'Heads up', style: 'head' });
    for (const w of input.warnings) lines.push({ text: `[${w.severity}] ${w.message}`, style: 'warn' });
  }
  lines.push({ text: 'Show me the math', style: 'head' });
  for (const s of input.steps) lines.push({ text: `${s.step}. ${s.description} = ${s.result}`, style: 'row' });
  lines.push({ text: 'Generated by SectorCalc — deterministic Decimal engine. Verify with the code edition in force.', style: 'note' });
  return lines;
}
