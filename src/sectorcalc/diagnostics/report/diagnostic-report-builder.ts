import type { DomainId } from "../diagnostic-types";
import { DOMAIN_REGISTRY } from "../domain-taxonomy";
import type { AnalyzeResponse } from "../diagnostic-service";
import type {
  DiagnosticReport,
  DomainSection,
  MeasurementSection,
  MeasurementSectionEntry,
  CostSection,
  DecisionSection,
  ActionPlanSection,
  EvidenceSection,
  AuditEvent,
  LimitationSection,
  ProblemSection,
} from "./diagnostic-report-types";
import type { MeasurementConfidenceInput, CostAtRiskInput } from "../diagnostic-types";
import {
  REPORT_TYPE,
  SCHEMA_VERSION,
  ENGINE_VERSION,
} from "./diagnostic-report-types";
import { buildMethodologySection, METHODOLOGY_VERSION } from "./diagnostic-report-methodology";
import { resolveRelatedTools } from "./related-tools-map";
import { createShortInputHash } from "./diagnostic-report-canonicalize";

const DISCLAIMER =
  "AI-assisted preliminary screening and decision-support output. Manual verification required by a qualified professional before any production action.";

const LLM_LIMITATION =
  "This report is generated by deterministic TypeScript engines. No large language model is used to calculate risk scores, costs, confidence values, or decisions. The output is preliminary decision-support only and does not constitute professional engineering judgment.";

/* ── Input shape that callers pass alongside the deterministic response ── */

export interface ReportBuilderInput {
  costs: CostAtRiskInput;
  measurements: MeasurementConfidenceInput[];
}

function buildDomainSection(domainId: string, response: AnalyzeResponse): DomainSection {
  const info = DOMAIN_REGISTRY[domainId as DomainId];
  if (!info) {
    throw new Error("Unknown domain: " + domainId);
  }
  return {
    domain_id: domainId as DomainId,
    label: info.label,
    description: info.description,
    category: info.category,
    process_description: response.domain.context.process_description,
    typical_tolerances: response.domain.context.typical_tolerances,
    common_defect_modes: response.domain.context.common_defect_modes,
  };
}

function buildProblemSection(response: AnalyzeResponse): ProblemSection {
  return { problem_context: response.problem_context };
}

function buildMeasurementSection(
  response: AnalyzeResponse,
  rawInputs?: MeasurementConfidenceInput[],
): MeasurementSection {
  const riskOrder: Record<string, number> = {
    BREACH: 4, UNCERTAIN: 3, NEAR_LIMIT: 2, INSIDE: 1,
  };
  let worstStatus = response.measurement_results[0]?.tolerance_status ?? "INSIDE";
  for (const mr of response.measurement_results) {
    if ((riskOrder[mr.tolerance_status] ?? 0) > (riskOrder[worstStatus] ?? 0)) {
      worstStatus = mr.tolerance_status;
    }
  }
  const entries: MeasurementSectionEntry[] = response.measurement_results.map((mr, i) => {
    const raw = rawInputs?.[i];
    return {
      index: mr.index,
      measured_value: mr.input_summary.measured_value,
      nominal_value: mr.input_summary.nominal_value,
      tolerance_plus: raw?.tolerance_plus ?? 0,
      tolerance_minus: raw?.tolerance_minus ?? 0,
      unit: mr.input_summary.unit,
      measurement_tool: mr.input_summary.tool,
      calibration_status: raw?.calibration_status ?? "",
      part_condition: raw?.part_condition ?? "",
      expanded_uncertainty_k2: mr.expanded_uncertainty_k2,
      confidence_class: mr.confidence_class,
      tolerance_status: mr.tolerance_status,
      mandatory_decision_floor: mr.mandatory_decision_floor,
    };
  });
  return { entries, worst_case_tolerance_status: worstStatus };
}

function buildCostSection(
  response: AnalyzeResponse,
  costInput?: CostAtRiskInput,
): CostSection {
  return {
    affected_quantity: costInput?.affected_quantity ?? 0,
    material_cost_per_unit: costInput?.material_cost_per_unit ?? 0,
    rework_hours_per_unit: costInput?.rework_hours_per_unit ?? 0,
    blended_hourly_rate: costInput?.blended_hourly_rate ?? 0,
    downtime_hours: costInput?.downtime_hours ?? 0,
    machine_hourly_rate: costInput?.machine_hourly_rate ?? 0,
    expedite_or_delay_cost: costInput?.expedite_or_delay_cost ?? 0,
    scrap_probability: costInput?.scrap_probability ?? 0,
    rework_probability: costInput?.rework_probability ?? 0,
    probability_source: response.cost_at_risk.probability_source,
    estimated_cost_at_risk: response.cost_at_risk.total,
    assumptions: response.cost_at_risk.assumptions,
  };
}

function buildDecisionSection(response: AnalyzeResponse): DecisionSection {
  return {
    total_risk_score: response.decision.total_risk_score,
    decision: response.decision.decision,
    mandatory_floor_applied: response.decision.mandatory_floor_applied,
    measurement_risk: response.decision.breakdown.measurement_risk,
    confidence_risk: response.decision.breakdown.confidence_risk,
    visual_advisory_risk: response.decision.breakdown.visual_advisory_risk,
    exposure_risk: response.decision.breakdown.exposure_risk,
    cost_risk: response.decision.breakdown.cost_risk,
    manual_check_risk: response.decision.breakdown.manual_check_risk,
  };
}

function buildActionPlanSection(response: AnalyzeResponse): ActionPlanSection {
  return {
    containment: response.action_plan.containment,
    temporary_fix: response.action_plan.temporary_fix,
    permanent_corrective_action: response.action_plan.permanent_corrective_action,
    required_manual_checks: response.action_plan.required_manual_checks,
  };
}

function buildEvidenceSection(privacyMode: string): EvidenceSection {
  return {
    photo_status: "NOT_ATTACHED",
    image_hash: null,
    privacy_mode: privacyMode === "strict" ? "privacy" : "standard",
  };
}

function buildLimitationSection(): LimitationSection {
  return {
    disclaimer: DISCLAIMER,
    llm_limitation: LLM_LIMITATION,
    manual_verification_required: true,
  };
}

function buildAuditLog(stringLog: string[]): AuditEvent[] {
  return stringLog.map((entry) => ({
    event: entry,
    at: new Date().toISOString(),
    source: "server" as const,
    engine_version: ENGINE_VERSION,
  }));
}

function buildCanonicalInput(response: AnalyzeResponse): Record<string, unknown> {
  return {
    domain_id: response.domain.id,
    problem_context: response.problem_context,
    measurements: response.measurement_results.map((mr) => ({
      measured_value: mr.input_summary.measured_value,
      nominal_value: mr.input_summary.nominal_value,
      tool: mr.input_summary.tool,
    })),
    cost_total: response.cost_at_risk.total,
    decision: response.decision.decision,
  };
}

export function buildDiagnosticReport(
  response: AnalyzeResponse,
  privacyMode: string,
  builderInput?: ReportBuilderInput,
): DiagnosticReport {
  const now = new Date().toISOString();
  const canonicalInput = buildCanonicalInput(response);
  const shortHash = createShortInputHash(canonicalInput);
  const reportId = "preview_" + shortHash;

  return {
    report_id: reportId,
    report_type: REPORT_TYPE,
    schema_version: SCHEMA_VERSION,
    engine_version: ENGINE_VERSION,
    methodology_version: METHODOLOGY_VERSION,
    created_at: now,
    domain_section: buildDomainSection(response.domain.id, response),
    problem_section: buildProblemSection(response),
    measurement_section: buildMeasurementSection(response, builderInput?.measurements),
    cost_section: buildCostSection(response, builderInput?.costs),
    decision_section: buildDecisionSection(response),
    action_plan_section: buildActionPlanSection(response),
    evidence_section: buildEvidenceSection(privacyMode),
    related_tools_section: resolveRelatedTools(response.domain.id),
    methodology_section: buildMethodologySection(),
    limitation_section: buildLimitationSection(),
    audit_log: buildAuditLog(response.audit_log),
  };
}
