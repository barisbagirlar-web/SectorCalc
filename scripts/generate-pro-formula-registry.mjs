// SectorCalc PRO Engine Governance — Auto-Generate Formula Registry
// Reads src/sectorcalc/formulas/pro-v531/*.formula.ts modules and generates
// a binding file that the API route can use. No PASS_THROUGH stubs.
//
// Expected output:
//   PRO_FORMULA_REGISTRY_GENERATED=PASS
//   LIVE_TOOLS=20
//   FORMULA_MODULES=20
//   REGISTRY_BINDINGS=20
//   MISSING_MODULES=0
//   PASS_THROUGH=0

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");

const ACTIVE_PRO_TOOL_SLUGS = [
  "break-even-survival-cash-calculator",
  "machine-hourly-rate-proof-report",
  "loss-making-job-detector",
  "receivables-cost-payment-term-addendum",
  "setup-time-reduction-roi-smed",
  "product-sku-margin-ranker",
  "true-employee-cost-statement",
  "job-quote-builder-pro-pack",
  "machine-investment-feasibility-buy-lease-keep",
  "capital-equipment-investment-appraisal-npv-irr",
  "customer-sku-profitability-forensics",
  "downtime-scrap-loss-statement",
  "oee-loss-monetization-improvement-business-case",
  "scrap-rework-cost-tracker",
  "outsource-vs-in-house-analyzer",
  "plant-wide-shop-rate-cost-structure-audit",
  "fx-commodity-pass-through-pricer",
  "energy-efficiency-grant-incentive-feasibility-pack",
  "motor-compressor-replacement-roi",
  "weld-procedure-cost-consumable-estimation-suite",
];

const formulaDir = path.join(root, "src/sectorcalc/formulas/pro-v531");
const outputPath = path.join(root, "src/sectorcalc/formulas/pro-v531/generated-registry.ts");

// Read all formula module files
const formulaFiles = fs.readdirSync(formulaDir)
  .filter(f => f.endsWith(".formula.ts"))
  .filter(f => f !== "compressed-air-leak-cost-calculator.formula.ts"); // excluded: typed-op only

const moduleNames = new Map(); // toolKey -> filename without ext

for (const f of formulaFiles) {
  const content = fs.readFileSync(path.join(formulaDir, f), "utf-8");
  const keyMatch = content.match(/export\s+const\s+toolKey\s*=\s*"([^"]+)"/);
  if (keyMatch) {
    moduleNames.set(keyMatch[1], f.replace(".formula.ts", ""));
  }
}

// Validate live tools have modules
const missingModules = ACTIVE_PRO_TOOL_SLUGS.filter(tk => !moduleNames.has(tk));
const hasMissing = missingModules.length > 0;

if (hasMissing) {
  console.log(`PRO_FORMULA_REGISTRY_GENERATED=FAIL`);
  console.log(`MISSING_MODULES=${missingModules.length}`);
  for (const mm of missingModules) {
    console.log(`  MISSING: ${mm}`);
  }
  process.exit(1);
}

// Generate the registry file
const importLines = [];
const registerLines = [];
let idx = 0;

for (const tk of ACTIVE_PRO_TOOL_SLUGS) {
  const modName = moduleNames.get(tk);
  const importName = `mod_${idx}`;
  importLines.push(`import * as ${importName} from "./${modName}.formula";`);
  registerLines.push(`  "${tk}": ${importName},`);
  idx++;
}

const generated = `// SectorCalc PRO Engine Governance — AUTO-GENERATED REGISTRY
// Generated by scripts/generate-pro-formula-registry.mjs
// Do not edit manually. Re-generate by running: node scripts/generate-pro-formula-registry.mjs
//
// LIVE_TOOLS=${ACTIVE_PRO_TOOL_SLUGS.length}
// GENERATED_AT=${new Date().toISOString()}

import type { ProFormulaModule } from "./resolve-formula-module";

${importLines.join("\n")}

export const GENERATED_FORMULA_REGISTRY: Record<string, ProFormulaModule> = {
${registerLines.join("\n")}
};

export const REGISTERED_TOOL_KEYS: readonly string[] = [
${ACTIVE_PRO_TOOL_SLUGS.map(tk => `  "${tk}",`).join("\n")}
];

export function getGeneratedToolKeys(): string[] {
  return [...REGISTERED_TOOL_KEYS];
}

export function getGeneratedRegistrySize(): number {
  return Object.keys(GENERATED_FORMULA_REGISTRY).length;
}
`;

fs.writeFileSync(outputPath, generated, "utf-8");

console.log(`PRO_FORMULA_REGISTRY_GENERATED=PASS`);
console.log(`LIVE_TOOLS=${ACTIVE_PRO_TOOL_SLUGS.length}`);
console.log(`FORMULA_MODULES=${moduleNames.size}`);
console.log(`REGISTRY_BINDINGS=${ACTIVE_PRO_TOOL_SLUGS.length}`);
console.log(`MISSING_MODULES=0`);
console.log(`PASS_THROUGH=0`);
console.log(`OUTPUT=${outputPath}`);
process.exit(0);
