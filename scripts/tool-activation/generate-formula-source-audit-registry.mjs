#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import { ROOT } from "./lib/activation-paths.mjs";
import {
  QUALITY_SCAN_REPORT_PATH,
  buildQualityScanReport,
  isRiskExcludedTool,
} from "./lib/quality-backfill-scan-lib.mjs";

const OUTPUT_FILE = path.join(
  ROOT,
  "src/lib/formula-governance/formula-source-audit-registry.ts",
);

const HARD_EXCLUDE_SLUGS = new Set([
  "cbam-compliance-verdict",
  "cbam-exposure-quick-check",
  "cbam-karbon-sinir-vergisi-ve-ihracat-maliyet-etkisi-calculator",
  "auto-repair-comeback-cost",
  "auto-repair-parts-labor-quote-calculator",
  "carbon-footprint-compliance-risk",
  "rent-vs-buy-calculator",
  "legal-interest-fee-calculator-pro",
  "electrical-panel-rework-cost",
  "pressure-vessel-wall-thickness-calculator",
  "break-even-safety-margin-calculator",
]);

function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, "utf8"));
}

function ensureQualityScan() {
  if (!fs.existsSync(QUALITY_SCAN_REPORT_PATH)) {
    const report = buildQualityScanReport();
    fs.mkdirSync(path.dirname(QUALITY_SCAN_REPORT_PATH), { recursive: true });
    fs.writeFileSync(QUALITY_SCAN_REPORT_PATH, `${JSON.stringify(report, null, 2)}\n`, "utf8");
  }
}

function isPremiumTier(tier) {
  return tier === "premium" || tier === "premium-schema";
}

function hasActiveRoute(tool) {
  return tool.hasRoute === true || tool.routeStatus === "active-route";
}

function shouldIncludeTool(tool) {
  if (!isPremiumTier(tool.tier)) return false;
  if (tool.upgradeDecision !== "PASS") return false;
  if (!hasActiveRoute(tool)) return false;
  if (HARD_EXCLUDE_SLUGS.has(tool.slug)) return false;
  if (isRiskExcludedTool(tool.slug, { riskLevel: tool.riskLevel })) return false;
  return true;
}

function buildRegistryEntry(tool) {
  return {
    slug: tool.slug,
    status: "verified",
    auditGate: "Formula Source-of-Truth Audit Gate",
    auditVersion: "v1",
    verifiedAtSource: "quality-scan-report",
    standardReference: "7 Muda quality standard",
    checks: {
      upgradeDecisionPass: tool.upgradeDecision === "PASS",
      hasFormulaContract: Boolean(tool.hasFormulaContract),
      hasValidation: Boolean(tool.hasValidation),
      hasTests: Boolean(tool.hasTests),
      hasQuickResult: Boolean(tool.hasQuickResult),
      hasDeepReport: Boolean(tool.hasDeepReport),
      hasI18n: Boolean(tool.hasI18n),
    },
  };
}

function renderRegistryFile(entries) {
  const body = entries
    .map((entry) => `  ${JSON.stringify(entry.slug)}: ${JSON.stringify(entry, null, 2).replace(/\n/g, "\n  ")},`)
    .join("\n");

  return `/**
 * Generated by scripts/tool-activation/generate-formula-source-audit-registry.mjs
 * Source: .sectorcalc/tool-quality/quality-scan-report.json (upgradeDecision === "PASS")
 * Do not edit manually — run: npm run audit:formula-registry
 */

export type FormulaSourceAuditStatus = {
  slug: string;
  status: "verified";
  auditGate: "Formula Source-of-Truth Audit Gate";
  auditVersion: "v1";
  verifiedAtSource: "quality-scan-report";
  standardReference: "7 Muda quality standard";
  checks: {
    upgradeDecisionPass: boolean;
    hasFormulaContract: boolean;
    hasValidation: boolean;
    hasTests: boolean;
    hasQuickResult: boolean;
    hasDeepReport: boolean;
    hasI18n: boolean;
  };
};

export const FORMULA_SOURCE_AUDIT_REGISTRY: Record<string, FormulaSourceAuditStatus> = {
${body}
};

export function getFormulaSourceAuditStatus(slug: string): FormulaSourceAuditStatus | null {
  return FORMULA_SOURCE_AUDIT_REGISTRY[slug] ?? null;
}

export function hasFormulaSourceAudit(slug: string): boolean {
  return Boolean(getFormulaSourceAuditStatus(slug));
}
`;
}

function main() {
  ensureQualityScan();
  const scanReport = readJson(QUALITY_SCAN_REPORT_PATH);
  const entries = (scanReport.tools ?? [])
    .filter(shouldIncludeTool)
    .map(buildRegistryEntry)
    .sort((left, right) => left.slug.localeCompare(right.slug));

  fs.mkdirSync(path.dirname(OUTPUT_FILE), { recursive: true });
  fs.writeFileSync(OUTPUT_FILE, renderRegistryFile(entries), "utf8");

  console.log(
    [
      "Formula Source Audit Registry generated",
      `verified: ${entries.length}`,
      `output: ${path.relative(ROOT, OUTPUT_FILE)}`,
    ].join("\n"),
  );
}

main();
