import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = path.join(path.dirname(fileURLToPath(import.meta.url)), "../..");
const P24_CACHE_PATH = path.join(ROOT, "scripts/.cache/p24-tool-quality-report.json");
const P24_VERDICTS_TS = path.join(ROOT, "src/lib/tools/runtime-readiness-p24-verdicts.ts");

const REVENUE_BOUNDARY_RESTORE_SLUGS = new Set([
  "auto-shop-margin-leak-detector",
  "change-order-impact-analyzer",
  "cnc-quote-risk-analyzer",
  "crop-yield-loss-analyzer",
  "dairy-profit-detector",
  "hvac-project-margin-guard",
  "landscaping-contract-profit-tool",
  "meal-planning-verdict",
  "menu-profit-leak-detector",
  "millwork-bid-risk-analyzer",
  "office-cleaning-bid-optimizer",
  "painting-job-profit-verdict",
  "panel-shop-margin-verdict",
  "plumbing-job-margin-verdict",
  "return-profit-erosion-tool",
  "roofing-contract-margin-guard",
  "sheet-metal-quote-risk-tool",
  "signage-bid-safe-price-tool",
  "water-optimization-verdict",
  "welding-bid-risk-analyzer",
]);

type P24ReportItem = {
  slug: string;
  verdict: string;
  routeStatus?: string;
  tier?: string;
};

function loadP24Maps(): { nonPass: Record<string, string>; explicitPass: string[] } {
  if (!fs.existsSync(P24_CACHE_PATH)) {
    return { nonPass: {}, explicitPass: [] };
  }
  const report = JSON.parse(fs.readFileSync(P24_CACHE_PATH, "utf8")) as {
    tools?: P24ReportItem[];
    items?: P24ReportItem[];
  };
  const verdictRank: Record<string, number> = {
    PASS: 0,
    WARN: 1,
    QUARANTINE: 2,
    FAIL: 3,
  };
  const mergedVerdicts: Record<string, string> = {};
  const rows = report.tools ?? report.items ?? [];
  for (const item of rows) {
    if (!item.verdict) {
      continue;
    }
    // Skip category-only stubs — they are catalog entries, not real tools.
    if (item.routeStatus === "category-only") {
      continue;
    }
    const current = mergedVerdicts[item.slug];
    if (!current || verdictRank[item.verdict] > verdictRank[current]) {
      mergedVerdicts[item.slug] = item.verdict;
    }
  }

  const nonPass: Record<string, string> = {};
  const explicitPass: string[] = [];
  for (const [slug, verdict] of Object.entries(mergedVerdicts)) {
    if (verdict === "PASS") {
      explicitPass.push(slug);
    } else {
      nonPass[slug] = verdict;
    }
  }
  for (const slug of REVENUE_BOUNDARY_RESTORE_SLUGS) {
    if (nonPass[slug] === "WARN") {
      delete nonPass[slug];
      if (!explicitPass.includes(slug)) {
        explicitPass.push(slug);
      }
    }
  }

  return { nonPass, explicitPass: explicitPass.sort((a, b) => a.localeCompare(b)) };
}

function writeP24VerdictsFile(nonPass: Record<string, string>, explicitPass: string[]): void {
  const nonPassEntries = Object.entries(nonPass)
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([slug, verdict]) => `  "${slug}": "${verdict}",`)
    .join("\n");

  const passEntries = explicitPass.map((slug) => `  "${slug}",`).join("\n");

  const content = `/**
 * P2.4 verdict cache for runtime readiness / trust gate.
 * Regenerated by: node scripts/tool-activation/audit-runtime-trust-engine.mjs
 * Non-PASS slugs block Formula Gate Approved badge.
 */

export type P24AuditVerdict = "PASS" | "WARN" | "FAIL" | "QUARANTINE";

/** Slugs with P2.4 verdict !== PASS (generated snapshot). */
export const P24_NON_PASS_VERDICTS: Readonly<Record<string, P24AuditVerdict>> = {
${nonPassEntries}
};

/** Slugs with explicit P2.4 PASS — required for Formula Gate trust eligibility. */
export const P24_EXPLICIT_PASS_SLUGS: ReadonlySet<string> = new Set([
${passEntries}
]);

import {
  isRevenueBoundaryP24TrustPass,
  resolveRevenueBoundaryP24Verdict,
} from "@/lib/tools/runtime-revenue-boundary-sync";

export function getP24VerdictForSlug(slug: string): P24AuditVerdict | "PASS" {
  return resolveRevenueBoundaryP24Verdict(slug, P24_NON_PASS_VERDICTS[slug]);
}

export function isP24PassForSlug(slug: string): boolean {
  return getP24VerdictForSlug(slug) === "PASS";
}

/** Stricter trust gate — unknown / unaudited slugs are not eligible. */
export function isP24TrustPassForSlug(slug: string): boolean {
  return isRevenueBoundaryP24TrustPass(
    slug,
    P24_NON_PASS_VERDICTS[slug],
    P24_EXPLICIT_PASS_SLUGS.has(slug),
  );
}
`;

  fs.writeFileSync(P24_VERDICTS_TS, content, "utf8");
}

function main(): void {
  const { nonPass, explicitPass } = loadP24Maps();
  writeP24VerdictsFile(nonPass, explicitPass);
  console.log("audit:runtime-trust-engine phase 1 PASS");
  console.log(`regenerated: ${path.relative(ROOT, P24_VERDICTS_TS)}`);
  console.log(`explicitPass: ${explicitPass.length}, nonPass: ${Object.keys(nonPass).length}`);
}

main();
