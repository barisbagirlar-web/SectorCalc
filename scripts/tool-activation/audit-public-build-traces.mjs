#!/usr/bin/env node
/**
 * Post-build gate — scan public-facing Next.js output for AI traces, placeholders,
 * prompt artifacts, and other brand-trust leaks.
 *
 * Usage (after `npm run build`):
 *   node scripts/tool-activation/audit-public-build-traces.mjs
 *
 * Does not mutate build output.
 */
import { existsSync, readFileSync, readdirSync, statSync } from "node:fs";
import { extname, join, relative } from "node:path";

const ROOT = join(import.meta.dirname, "..", "..");

const TEXT_EXTENSIONS = new Set([
  ".html",
  ".htm",
  ".css",
  ".js",
  ".json",
  ".jsonl",
  ".txt",
  ".xml",
  ".svg",
  ".webmanifest",
]);

/** Patterns applied to full file text (public assets, css, json, xml, txt). */
const FULL_TEXT_RULES = [
  { id: "template-currency", re: /\{currency\}/g },
  { id: "template-unit", re: /\{unit:[a-z][a-z0-9_%-]*\}/gi },
  { id: "todo", re: /\bTODO\b/g },
  { id: "fixme", re: /\bFIXME\b/g },
  { id: "placeholder-text", re: /\bplaceholder\b/gi },
  { id: "lorem-ipsum", re: /\blorem ipsum\b/gi },
  { id: "ai-generated", re: /\bAI generated\b/gi },
  { id: "chatgpt", re: /\bChatGPT(?!-User)\b/gi },
  { id: "prompt-artifact", re: /\bprompt artifact\b/gi },
  { id: "dummy", re: /\bdummy\b/gi },
  { id: "test-customer", re: /\btest customer\b/gi },
  { id: "as-an-ai", re: /\bas an AI\b/gi },
  { id: "generated-by-ai", re: /\bgenerated by AI\b/gi },
];

/** High-signal only — vendor JS may contain internal TODO strings and i18n templates. */
const JS_HIGH_SIGNAL_RULES = [
  { id: "lorem-ipsum", re: /\blorem ipsum\b/gi },
  { id: "ai-generated", re: /\bAI generated\b/gi },
  { id: "chatgpt", re: /\bChatGPT(?!-User)\b/gi },
  { id: "prompt-artifact", re: /\bprompt artifact\b/gi },
  { id: "test-customer", re: /\btest customer\b/gi },
  { id: "as-an-ai", re: /\bas an AI\b/gi },
  { id: "generated-by-ai", re: /\bgenerated by AI\b/gi },
];

const HTML_VISIBLE_RULES = FULL_TEXT_RULES;

/** CSS may legitimately reference ::placeholder and Tailwind placeholder: utilities. */
const CSS_RULES = FULL_TEXT_RULES.filter((rule) => rule.id !== "placeholder-text");

const findings = [];
const scannedDirs = [];

function rel(path) {
  return relative(ROOT, path).split("\\").join("/");
}

function stripHtmlForVisibleText(html) {
  let source = html.replace(/<!--[\s\S]*?-->/g, "");
  source = source.replace(/<script[\s\S]*?<\/script>/gi, "");
  source = source.replace(/<style[\s\S]*?<\/style>/gi, "");
  source = source.replace(/<[^>]+>/g, " ");
  return source.replace(/\s+/g, " ");
}

function stripCssPseudoPlaceholders(source) {
  return source
    .replace(/::[-a-z]*placeholder/gi, "")
    .replace(/:placeholder-shown/gi, "");
}

function collectMatches(source, rules, filePath) {
  for (const rule of rules) {
    rule.re.lastIndex = 0;
    const match = rule.re.exec(source);
    if (match) {
      findings.push({
        rule: rule.id,
        file: rel(filePath),
        sample: match[0].slice(0, 120),
      });
    }
  }
}

function walkFiles(directory, onFile, options = {}) {
  if (!existsSync(directory)) {
    return;
  }

  for (const entry of readdirSync(directory, { withFileTypes: true })) {
    const fullPath = join(directory, entry.name);

    if (entry.isDirectory()) {
      if (entry.name === "cache" || entry.name === "node_modules") {
        continue;
      }
      if (options.skipMetadataRouteDirs) {
        const name = entry.name;
        if (
          name.endsWith(".txt") ||
          name.endsWith(".xml") ||
          name.endsWith(".png") ||
          name === "icon.png" ||
          name === "apple-icon.png"
        ) {
          continue;
        }
      }
      walkFiles(fullPath, onFile, options);
      continue;
    }

    if (!entry.isFile()) {
      continue;
    }

    const extension = extname(entry.name).toLowerCase();
    if (extension === ".map" || extension === ".nft.json" || entry.name.endsWith(".meta")) {
      continue;
    }

    onFile(fullPath, extension);
  }
}

function scanPublicDirectory() {
  const publicDir = join(ROOT, "public");
  if (!existsSync(publicDir)) {
    return;
  }

  scannedDirs.push("public");
  walkFiles(publicDir, (filePath, extension) => {
    if (!TEXT_EXTENSIONS.has(extension)) {
      return;
    }
    const source = readFileSync(filePath, "utf8");
    collectMatches(source, FULL_TEXT_RULES, filePath);
  });
}

function scanNextStatic() {
  const cssDir = join(ROOT, ".next", "static", "css");
  const chunksDir = join(ROOT, ".next", "static", "chunks");

  if (existsSync(cssDir)) {
    scannedDirs.push(".next/static/css");
    walkFiles(cssDir, (filePath, extension) => {
      if (extension !== ".css") {
        return;
      }
      collectMatches(
        stripCssPseudoPlaceholders(readFileSync(filePath, "utf8")),
        CSS_RULES,
        filePath,
      );
    });
  }

  if (existsSync(chunksDir)) {
    scannedDirs.push(".next/static/chunks");
    walkFiles(chunksDir, (filePath, extension) => {
      if (extension !== ".js") {
        return;
      }
      collectMatches(readFileSync(filePath, "utf8"), JS_HIGH_SIGNAL_RULES, filePath);
    });
  }
}

function scanNextServerHtml() {
  const appDir = join(ROOT, ".next", "server", "app");
  if (!existsSync(appDir)) {
    return;
  }

  scannedDirs.push(".next/server/app");
  walkFiles(
    appDir,
    (filePath, extension) => {
      if (extension === ".html") {
        collectMatches(
          stripHtmlForVisibleText(readFileSync(filePath, "utf8")),
          HTML_VISIBLE_RULES,
          filePath,
        );
        return;
      }

      if (
        filePath.endsWith(".body") &&
        (filePath.endsWith("robots.txt.body") || filePath.endsWith("sitemap.xml.body"))
      ) {
        collectMatches(readFileSync(filePath, "utf8"), FULL_TEXT_RULES, filePath);
      }
    },
    { skipMetadataRouteDirs: true },
  );
}

function scanOutDirectory() {
  const outDir = join(ROOT, "out");
  if (!existsSync(outDir)) {
    return;
  }

  scannedDirs.push("out");
  walkFiles(outDir, (filePath, extension) => {
    if (!TEXT_EXTENSIONS.has(extension)) {
      return;
    }

    const raw = readFileSync(filePath, "utf8");
    if (extension === ".html" || extension === ".htm") {
      collectMatches(stripHtmlForVisibleText(raw), HTML_VISIBLE_RULES, filePath);
      return;
    }

    if (extension === ".js") {
      collectMatches(raw, JS_HIGH_SIGNAL_RULES, filePath);
      return;
    }

    collectMatches(raw, FULL_TEXT_RULES, filePath);
  });
}

function main() {
  const nextDir = join(ROOT, ".next");
  if (!existsSync(nextDir) || !statSync(nextDir).isDirectory()) {
    console.error("FAIL: .next build output missing. Run `npm run build` first.");
    process.exit(1);
  }

  scanPublicDirectory();
  scanNextStatic();
  scanNextServerHtml();
  scanOutDirectory();

  console.log("=== Public Build Trace Audit ===");
  console.log(`Root: ${ROOT}`);
  console.log(`Scanned directories: ${scannedDirs.length > 0 ? scannedDirs.join(", ") : "(none)"}`);

  if (findings.length === 0) {
    console.log("\nPASS: No public build trace risks found.");
    process.exit(0);
  }

  console.error(`\nFAIL: ${findings.length} public build trace risk(s) found.\n`);
  for (const item of findings) {
    console.error(`- [${item.rule}] ${item.file} → "${item.sample}"`);
  }
  process.exit(1);
}

main();
