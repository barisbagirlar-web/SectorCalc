import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { evaluateRuntimeTrust } from "../../src/lib/tools/runtime-trust-engine";
import { hasFormulaSourceAudit } from "../../src/lib/formula-governance/formula-source-audit-registry";
import { listAllPremiumToolRouteSlugs } from "../../src/lib/tools/free-traffic-routes";
import { getPremiumRevenueRouteSlugs } from "../../src/lib/tools/revenue-tools";
import { listPremiumSchemaSlugs } from "../../src/lib/premium-schema/schemas/index";

const ROOT = path.join(path.dirname(fileURLToPath(import.meta.url)), "../..");
const REPORT_PATH = path.join(ROOT, "scripts/.cache/runtime-trust-engine-report.json");
const P24_CACHE_PATH = path.join(ROOT, "scripts/.cache/p24-tool-quality-report.json");
const P24_VERDICTS_TS = path.join(ROOT, "src/lib/tools/runtime-readiness-p24-verdicts.ts");

type AuditItem = {
  slug: string;
  route: string;
  tier: string;
  locale: string;
  status: "ready" | "review" | "blocked";
  formulaGateEligible: boolean;
  paymentEligible: boolean;
  calculationEligible: boolean;
  findings: string[];
  recommendedAction: string;
};

function loadP24Maps(): { nonPass: Record<string, string>; explicitPass: string[] } {
  if (!fs.existsSync(P24_CACHE_PATH)) {
    return { nonPass: {}, explicitPass: [] };
  }
  const report = JSON.parse(fs.readFileSync(P24_CACHE_PATH, "utf8")) as {
    tools?: Array<{ slug: string; verdict: string }>;
    items?: Array<{ slug: string; verdict: string }>;
  };
  const verdictRank: Record<string, number> = {
    PASS: 0,
    WARN: 1,
    QUARANTINE: 2,
    FAIL: 3,
  };
  const mergedVerdicts: Record<string, string> = {};
  const rows = report.tools ?? report.items ?? [];
  for (const item of rows) {
    if (!item.verdict) {
      continue;
    }
    const current = mergedVerdicts[item.slug];
    if (!current || verdictRank[item.verdict] > verdictRank[current]) {
      mergedVerdicts[item.slug] = item.verdict;
    }
  }

  const nonPass: Record<string, string> = {};
  const explicitPass: string[] = [];
  for (const [slug, verdict] of Object.entries(mergedVerdicts)) {
    if (verdict === "PASS") {
      explicitPass.push(slug);
    } else {
      nonPass[slug] = verdict;
    }
  }
  return { nonPass, explicitPass: explicitPass.sort((a, b) => a.localeCompare(b)) };
}

function writeP24VerdictsFile(nonPass: Record<string, string>, explicitPass: string[]): void {
  const nonPassEntries = Object.entries(nonPass)
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([slug, verdict]) => `  "${slug}": "${verdict}",`)
    .join("\n");

  const passEntries = explicitPass.map((slug) => `  "${slug}",`).join("\n");

  const content = `/**
 * P2.4 verdict cache for runtime readiness / trust gate.
 * Regenerated by: node scripts/tool-activation/audit-runtime-trust-engine.mjs
 * Non-PASS slugs block Formula Gate Approved badge.
 */

export type P24AuditVerdict = "PASS" | "WARN" | "FAIL" | "QUARANTINE";

/** Slugs with P2.4 verdict !== PASS (generated snapshot). */
export const P24_NON_PASS_VERDICTS: Readonly<Record<string, P24AuditVerdict>> = {
${nonPassEntries}
};

/** Slugs with explicit P2.4 PASS — required for Formula Gate trust eligibility. */
export const P24_EXPLICIT_PASS_SLUGS: ReadonlySet<string> = new Set([
${passEntries}
]);

export function getP24VerdictForSlug(slug: string): P24AuditVerdict | "PASS" {
  return P24_NON_PASS_VERDICTS[slug] ?? "PASS";
}

export function isP24PassForSlug(slug: string): boolean {
  return getP24VerdictForSlug(slug) === "PASS";
}

/** Stricter trust gate — unknown / unaudited slugs are not eligible. */
export function isP24TrustPassForSlug(slug: string): boolean {
  if (P24_NON_PASS_VERDICTS[slug]) {
    return false;
  }
  return P24_EXPLICIT_PASS_SLUGS.has(slug);
}
`;

  fs.writeFileSync(P24_VERDICTS_TS, content, "utf8");
}

function collectSlugs(): string[] {
  const slugs = new Set<string>([
    ...listAllPremiumToolRouteSlugs(),
    ...getPremiumRevenueRouteSlugs(),
    ...listPremiumSchemaSlugs(),
  ]);
  return [...slugs].sort((a, b) => a.localeCompare(b));
}

function main(): void {
  const { nonPass, explicitPass } = loadP24Maps();
  writeP24VerdictsFile(nonPass, explicitPass);

  const slugs = collectSlugs();
  const items: AuditItem[] = [];

  for (const slug of slugs) {
    const decision = evaluateRuntimeTrust({
      slug,
      locale: "tr",
      surface: "premium",
      premiumSurfaceUsesFreeCopy: false,
    });
    items.push({
      slug,
      route: decision.route,
      tier: decision.tier,
      locale: decision.locale,
      status: decision.status,
      formulaGateEligible: decision.formulaGateEligible,
      paymentEligible: decision.paymentEligible,
      calculationEligible: decision.calculationEligible,
      findings: [...decision.findings],
      recommendedAction: decision.recommendedAction,
    });
  }

  const falseBadgeRegistry = items.filter(
    (item) => hasFormulaSourceAudit(item.slug) && !item.formulaGateEligible,
  );

  const report = {
    generatedAt: new Date().toISOString(),
    totalChecked: items.length,
    ready: items.filter((item) => item.status === "ready").length,
    review: items.filter((item) => item.status === "review").length,
    blocked: items.filter((item) => item.status === "blocked").length,
    formulaGateEligible: items.filter((item) => item.formulaGateEligible).length,
    paymentEligible: items.filter((item) => item.paymentEligible).length,
    falseBadgeWouldHaveShown: falseBadgeRegistry.length,
    items,
  };

  fs.mkdirSync(path.dirname(REPORT_PATH), { recursive: true });
  fs.writeFileSync(REPORT_PATH, `${JSON.stringify(report, null, 2)}\n`, "utf8");

  const risky = items.filter((item) => !item.formulaGateEligible).slice(0, 20);
  const problem = items.find((item) => item.slug === "abonelik-yazilim-cloud-yillik-maliyet-hesabi");

  console.log("audit:runtime-trust-engine PASS");
  console.log(`totalChecked: ${report.totalChecked}`);
  console.log(`ready: ${report.ready}`);
  console.log(`review: ${report.review}`);
  console.log(`blocked: ${report.blocked}`);
  console.log(`formulaGateEligible: ${report.formulaGateEligible}`);
  console.log(`paymentEligible: ${report.paymentEligible}`);
  console.log(`falseBadgeWouldHaveShown: ${report.falseBadgeWouldHaveShown}`);
  console.log(`output: ${path.relative(ROOT, REPORT_PATH)}`);
  console.log("\nTop risky tools:");
  for (const item of risky) {
    console.log(` - ${item.slug} (${item.tier}) ${item.status} → ${item.findings.join(", ")}`);
  }
  if (problem) {
    console.log("\nProblem slug:");
    console.log(
      ` abonelik-yazilim-cloud-yillik-maliyet-hesabi → ${problem.status}, formulaGateEligible=${problem.formulaGateEligible}, paymentEligible=${problem.paymentEligible}, findings=${problem.findings.join(", ")}`,
    );
  }
  if (falseBadgeRegistry.length > 0) {
    console.log("\nRegistry audit but trust-ineligible (badge blocked):");
    for (const item of falseBadgeRegistry.slice(0, 10)) {
      console.log(` - ${item.slug}`);
    }
  }
}

main();
