def get_name_part(title, locale):
    """Strip the calculator suffix to get the name portion."""
    for suffix in LOCALE_SUFFIXES.get(locale, []):
        if title.endswith(suffix):
            return title[:-len(suffix)].strip()
    # Also try common English suffixes
    for suffix in [" Calculator", " calculator"]:
        if title.endswith(suffix):
            return title[:-len(suffix)].strip()
    return title.strip()

def is_acronym(word):
    """Check if word should be preserved (acronym/abbreviation)."""
    return word in PRESERVE

def translate_name(en_name, locale):
    """Translate an English calculator name to the target locale."""
    trans_dict = LOCALE_DICTS.get(locale, {})
    
    # Handle empty
    if not en_name:
        return en_name
    
    # Tokenize preserving numbers and special tokens
    tokens = re.findall(r'[A-Za-z0-9_.]+|[^\w\s]|\s+', en_name)
    
    result_tokens = []
    i = 0
    while i < len(tokens):
        token = tokens[i]
        
        # Preserve whitespace as-is
        if token.isspace():
            result_tokens.append(token)
            i += 1
            continue
        
        # Preserve punctuation
        if re.match(r'^[^\w\s]$', token):
            result_tokens.append(token)
            i += 1
            continue
        
        # Check if it's a number (like 5k, 10k, 401k)
        if re.match(r'^[\d]+[kKmM]?$', token):
            result_tokens.append(token)
            i += 1
            continue
        
        # Check if it's a number-only token like 1031, 1099, 529, etc.
        if re.match(r'^[\d]+$', token):
            result_tokens.append(token)
            i += 1
            continue
        
        # Handle compound numbers with letters: 3d, 5x5, etc.
        if re.match(r'^[\d]+[xX][\d]+$', token):
            result_tokens.append(token)
            i += 1
            continue
        
        # Check if it's an acronym or special abbreviation
        if token.upper() in PRESERVE or token in PRESERVE:
            result_tokens.append(token)
            i += 1
            continue
        
        # Try to translate word/phrase (checking for multi-word matches first)
        # Check up to 3-word phrases
        translated = False
        for phrase_len in [3, 2, 1]:
            if i + phrase_len <= len(tokens):
                phrase = ''.join(tokens[i:i+phrase_len]).strip()
                if phrase in trans_dict:
                    result = trans_dict[phrase]
                    if result:  # Only append non-empty
                        result_tokens.append(result)
                    translated = True
                    i += phrase_len
                    break
        
        if not translated:
            # Keep original if no translation found
            result_tokens.append(token)
            i += 1
    
    result = ''.join(result_tokens).strip()
    # Clean up multiple spaces
    result = re.sub(r'\s+', ' ', result)
    return result


