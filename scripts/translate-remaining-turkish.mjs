const schemaFiles = globSync("generated/schemas/**/*.json");

for (const file of schemaFiles) {
  let content = fs.readFileSync(file, "utf8");
  let changed = false;
  
  // A simple replace all for exact JSON keys matching these translations
  for (const [tr, en] of Object.entries(translations)) {
    const searchString = `"en": "${tr}"`;
    const replaceString = `"en": "${en}"`;
    if (content.includes(searchString)) {
      content = content.replace(new RegExp(searchString.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'g'), replaceString);
      changed = true;
    }
  }

  if (changed) {
    fs.writeFileSync(file, content);
    console.log(`Updated ${file}`);
  }
}

