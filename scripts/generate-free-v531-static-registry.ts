/**
 * scripts/generate-free-v531-static-registry.ts
 *
 * V5.4 Core — Generate bundle-safe static registry for Free V5.3.1 schemas.
 *
 * Reads all 295-344.json schema files, generates registry.generated.ts
 * with build-time JSON imports so no runtime fs/dir scanning is needed.
 * Maps by schema.tool_key.
 *
 * Run: npx tsx scripts/generate-free-v531-static-registry.ts
 * Then: tsc must see the generated .ts file
 */

import { readFileSync, writeFileSync, existsSync, readdirSync } from "node:fs";
import { join } from "node:path";

const ROOT = process.cwd();
const SCHEMA_DIR = join(ROOT, "src/sectorcalc/schemas/free-v531");
const OUTPUT_FILE = join(SCHEMA_DIR, "registry.generated.ts");

// Known V5.3.1 slug set (50 tools + legacy break-even-and-margin-of-safety)
const V531_SLUGS = new Set([
  "machining-cost-per-part",
  "cnc-shop-hourly-rate",
  "cutting-speed-feed-rpm",
  "tap-drill-size",
  "iso-286-tolerance-fit",
  "surface-roughness-converter",
  "material-removal-rate",
  "tool-life-tool-cost-per-part",
  "scrap-cost",
  "rework-vs-scrap-decision",
  "thread-dimensions-reference",
  "knurling-drill-point-depth",
  "weld-metal-weight-consumable",
  "fillet-weld-size-strength",
  "welding-cost-per-meter",
  "welding-heat-input",
  "bolt-torque",
  "bolt-preload-clamp-force",
  "steel-weight",
  "beam-load-deflection-quick-check",
  "sheet-metal-bend-allowance",
  "oee",
  "downtime-cost",
  "takt-time-cycle-time",
  "setup-time-cost",
  "line-balancing-efficiency",
  "compressed-air-leak-cost",
  "electric-motor-running-cost",
  "energy-cost-per-part",
  "cbam-cost-quick-estimator",
  "electricity-co2-emissions",
  "diesel-fuel-co2-emissions",
  "product-carbon-footprint-basic",
  "carbon-price-exposure",
  "true-employee-cost",
  "quote-margin-markup",
  "break-even-point",
  "payment-term-cost",
  "machine-investment-payback",
  "customer-profitability",
  "currency-adjusted-pricing",
  "eoq",
  "safety-stock-reorder-point",
  "inventory-carrying-cost",
  "pallet-container-load-cbm",
  "freight-cost-per-km-trip",
  "concrete-volume-order-quantity",
  "rebar-weight-count",
  "recipe-cost-menu-price",
  "fabric-consumption-gsm",
]);

interface SchemaMeta {
  fileName: string;
  toolKey: string;
  fileIndex: number;
}

function main(): void {
  if (!existsSync(SCHEMA_DIR)) {
    console.error("FATAL: schema directory not found:", SCHEMA_DIR);
    process.exit(1);
  }

  // Collect all JSON files, match against V531 slugs by tool_key
  const files = readdirSync(SCHEMA_DIR)
    .filter((f: string) => f.endsWith(".json"))
    .sort();

  const entries: Array<{ toolKey: string; slugSafe: string; fileName: string }> = [];

  for (const fn of files) {
    const raw = JSON.parse(readFileSync(join(SCHEMA_DIR, fn), "utf-8")) as {
      tool_key?: string;
    };
    const tk = raw?.tool_key;
    if (tk && V531_SLUGS.has(tk)) {
      const slugSafe = tk.replace(/[^a-zA-Z0-9_]/g, "_");
      entries.push({ toolKey: tk, slugSafe, fileName: fn });
    }
  }

  if (entries.length === 0) {
    console.error(
      "FATAL: no V5.3.1 schemas found in",
      SCHEMA_DIR,
      "Check that 295-344.json files exist with correct tool_key",
    );
    process.exit(1);
  }

  // Generate import lines and map entries
  const importLines: string[] = [];
  const mapEntries: string[] = [];
  const slugList: string[] = [];

  for (const { toolKey, slugSafe, fileName } of entries) {
    const varName = `s_${slugSafe}`;
    // Strip .json extension for TS module import
    const importPath = `./${fileName}`;
    importLines.push(`import ${varName} from "${importPath}";`);
    mapEntries.push(`  ["${toolKey}", ${varName} as unknown as SchemaType]`);
    slugList.push(`  "${toolKey}"`);
  }

  const output = `// ⚡ AUTO-GENERATED — DO NOT EDIT
// Generated by: npx tsx scripts/generate-free-v531-static-registry.ts
// Purpose: Bundle-safe static Free V5.3.1 schema registry
// Map key: schema.tool_key
// Generated on: ${new Date().toISOString().split("T")[0]}

import type { SuperV4Schema } from "@/sectorcalc/pro-form/contract-types";

type SchemaType = SuperV4Schema;

${importLines.join("\n")}

/**
 * Bundle-safe static Free V5.3.1 schema map.
 * Keyed by tool_key (hyphenated slug).
 * Imported at build time — no runtime fs or directory scanning.
 */
export const FREE_V531_SCHEMA_BY_SLUG: ReadonlyMap<string, SchemaType> = new Map([
${mapEntries.join(",\n")},
]);

/**
 * Sorted list of all Free V5.3.1 schema slugs.
 */
export const FREE_V531_SCHEMA_SLUGS: readonly string[] = [
${slugList.join(",\n")},
] as const;

/**
 * Resolve a Free V5.3.1 schema by slug.
 * Returns the schema or undefined if not found.
 */
export function getFreeV531SchemaBySlug(slug: string): SchemaType | undefined {
  return FREE_V531_SCHEMA_BY_SLUG.get(slug);
}

/**
 * Check if a Free V5.3.1 schema exists for the given slug.
 */
export function hasFreeV531Schema(slug: string): boolean {
  return FREE_V531_SCHEMA_BY_SLUG.has(slug);
}
`;

  writeFileSync(OUTPUT_FILE, output, "utf-8");
  console.log(`\n✅ Generated: ${OUTPUT_FILE}`);
  console.log(`   Entries: ${entries.length}`);
  console.log(`   Slugs:`);
  for (const e of entries) {
    console.log(`     - ${e.toolKey}`);
  }
}

main();
